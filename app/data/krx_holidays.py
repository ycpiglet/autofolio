"""KRX 공식 휴장일 정적 캘린더.

한국거래소(KRX) 공식 휴장일 목록 — 2025·2026년.
출처: KRX 공식 공고 + 관공서 공휴일 규정.
이 목록은 연초에 KRX가 발표하는 공식 휴장일 공고를 반영한다.
대체공휴일(代替公休日)은 공휴일법에 따라 계산되며, 불확실한 일부 항목은
주석으로 표기한다. 매년 갱신 필요.
"""
